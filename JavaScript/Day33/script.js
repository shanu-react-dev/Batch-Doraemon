console.log("This is conditional Statement using Switch")
let day = "Wednesday"

switch(day){
    case "Monday":
        console.log("Today is Monday")
        break;
    case "Tuesday":
        console.log("Today is Tuesday")
        break;
    case "Wednesday":
        console.log("Today is Wednesday")
        break;
    case "Thursday":
        console.log("Today is Thursday")
        break;
    case "Friday":
        console.log("Today is Friday")
        break;
    case "Saturday":
        console.log("Today is Weekend Enjoy like a hell")
        break;
    case "Sunday":
        console.log("Today is Funday Only sleeping")
        break;
    default:
        console.log("Enter a valid day name")
}
